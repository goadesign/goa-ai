package framework

import (
	"os"
	"path/filepath"
	"testing"

	"github.com/stretchr/testify/require"
)

func TestCleanGeneratedExampleArtifacts(t *testing.T) {
	root := t.TempDir()
	cmdDir := filepath.Join(root, "cmd", "assistant")
	require.NoError(t, os.MkdirAll(cmdDir, 0o755))
	require.NoError(t, os.WriteFile(filepath.Join(cmdDir, "main.go"), []byte("package main\n"), 0o644))
	require.NoError(t, os.WriteFile(
		filepath.Join(root, "generated_test.go"),
		[]byte("// Code generated by goa\npackage assistant\n"),
		0o644,
	))
	require.NoError(t, os.WriteFile(
		filepath.Join(root, "manual_test.go"),
		[]byte("package assistant\n"),
		0o644,
	))

	err := cleanGeneratedExampleArtifacts(root)

	require.NoError(t, err)
	_, statErr := os.Stat(filepath.Join(root, "cmd"))
	require.ErrorIs(t, statErr, os.ErrNotExist)
	_, statErr = os.Stat(filepath.Join(root, "generated_test.go"))
	require.ErrorIs(t, statErr, os.ErrNotExist)
	_, statErr = os.Stat(filepath.Join(root, "manual_test.go"))
	require.NoError(t, statErr)
}
